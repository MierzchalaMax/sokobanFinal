
function levelMaker(lvlNbr){
    // Native map
    const level = new Array();
    level.push('--####|--#--#|###-.#|#--*-#|#-#@-#|#-$*-#|##---#|-#####');
    level.push('######-|#----#-|#-#.-##|#--$$-#|###*+-#|--#---#|--#####');
    level.push('-####--|-#-@#--|##$*###|#--.--#|#-#.--#|#-$--##|###--#-|--####-');
    level.push('-#####-|-#---#-|##..$##|#--.--#|#--$$-#|####@-#|--####-');
    level.push('#######|#-.---#|#-#$--#|#-$.#-#|##.$@-#|-#--###|-#--#--|-####--');
    level.push('-#####--|-#---###|##-#---#|#@$.-.-#|#-$*#-##|##----#-|-######-');
    level.push('-#####--|-#---##-|-#-#-.##|##$-.--#|#@$.$--#|#---####|#####---');
    level.push('-####--|##--###|#--$--#|#-+$--#|##-$###|-#.-.-#|-#----#|-######');
    level.push('--####-|-##--#-|##-.-##|#-$*$-#|#-#.--#|#--@--#|#######');
    level.push('#####--|#---###|#-$@--#|##*-.-#|#-*-#-#|#-----#|#######');
    level.push('--#####-|####---#|#-$--#.#|#-#*---#|#--@$.-#|###--###|--####--');
    level.push('--####-|###--#-|#-+*-##|#-#$--#|#---#-#|#---*-#|#######');
    level.push('-####--|##--#--|#--$###|#-.$--#|#-.*--#|###@--#|--#####');
    level.push('---####-|--##--#-|###-.$#-|#--$.-##|#@-$.--#|####---#|---#####');
    level.push('--#####-|###---#-|#--$#.##|#-#.---#|#---*@-#|####$-##|---#--#-|---####-');
    level.push('#####---|#---##--|#-#@-###|#--**.-#|#-$--#-#|####---#|---#####');
    level.push('#####--|#---###|#---$-#|##$*--#|-#-..##|-#@-##-|-####--');
    level.push('######--|#--.-###|#-$$$@-#|#-.#.--#|#----###|######--');
    level.push('--####--|###--#--|#@.-$#--|#-#--###|#-*-*--#|###----#|--######');
    level.push('####---|#--####|#---$-#|#-*.#-#|##$.--#|-#@-###|-#--#--|-####--');
    // String --> Array[Array[]]
    var returnArray = new Array();
    var array1 = level[lvlNbr].split('|');
    for(let item of array1){
        returnArray.push(item.split(''));
    }
    return returnArray;
}

